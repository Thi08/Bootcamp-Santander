//Utilize o método "shift" para remover o primeiro elemento do array.

const linguagens = ["javascript", "python", "golang", "java"];

linguagens.shift();

//Verifique o comprimento (length) do array para confirmar que o elemento foi removido corretamente.

console.log(linguagens.length);

//Utilize o método "shift" novamente para remover outro elemento do array.

linguagens.shift();

//Verifique o comprimento (length) do array novamente para confirmar que o segundo elemento foi removido corretamente.
console.log(linguagens.length);
